const RCON: [u8; 11] = [0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36];

/// Cle AES etendue (128 ou 256 bits).
pub struct Aes {
    round_keys: [[u8; 16]; 15],
    rounds: usize,
}

fn xtime(a: u8) -> u8 {
    let hi = a & 0x80;
    let mut r = a << 1;
    if hi != 0 { r ^= 0x1b; }
    r
}

fn gmul(a: u8, b: u8) -> u8 {
    let mut a = a;
    let mut b = b;
    let mut p = 0u8;
    for _ in 0..8 {
        if b & 1 != 0 { p ^= a; }
        a = xtime(a);
        b >>= 1;
    }
    p
}

impl Aes {
    /// Initialise depuis une cle de 16 (AES-128) ou 32 (AES-256) octets.
    pub fn new(key: &[u8]) -> Aes {
        let (nk, rounds) = match key.len() {
            16 => (4, 10),
            32 => (8, 14),
            _ => (4, 10),
        };
        let total_words = 4 * (rounds + 1);
        let mut w = [[0u8; 4]; 60];
        for i in 0..nk {
            w[i] = [key[4 * i], key[4 * i + 1], key[4 * i + 2], key[4 * i + 3]];
        }
        for i in nk..total_words {
            let mut temp = w[i - 1];
            if i % nk == 0 {
                // RotWord + SubWord + Rcon
                temp = [temp[1], temp[2], temp[3], temp[0]];
                for t in temp.iter_mut() { *t = SBOX[*t as usize]; }
                temp[0] ^= RCON[i / nk];
            } else if nk > 6 && i % nk == 4 {
                for t in temp.iter_mut() { *t = SBOX[*t as usize]; }
            }
            for j in 0..4 {
                w[i][j] = w[i - nk][j] ^ temp[j];
            }
        }
        let mut round_keys = [[0u8; 16]; 15];
        for r in 0..(rounds + 1) {
            for c in 0..4 {
                for j in 0..4 {
                    round_keys[r][4 * c + j] = w[4 * r + c][j];
                }
            }
        }
        Aes { round_keys, rounds }
    }

    /// Chiffre un bloc de 16 octets en place.
    pub fn encrypt_block(&self, block: &mut [u8; 16]) {
        add_round_key(block, &self.round_keys[0]);
        for r in 1..self.rounds {
            sub_bytes(block);
            shift_rows(block);
            mix_columns(block);
            add_round_key(block, &self.round_keys[r]);
        }
        sub_bytes(block);
        shift_rows(block);
        add_round_key(block, &self.round_keys[self.rounds]);
    }
}

fn add_round_key(s: &mut [u8; 16], rk: &[u8; 16]) {
    for i in 0..16 { s[i] ^= rk[i]; }
}

fn sub_bytes(s: &mut [u8; 16]) {
    for i in 0..16 { s[i] = SBOX[s[i] as usize]; }
}

// Etat AES = colonnes : octet (r,c) a l'index c*4+r.
fn shift_rows(s: &mut [u8; 16]) {
    let t = *s;
    // ligne 1 decalee de 1
    s[1] = t[5]; s[5] = t[9]; s[9] = t[13]; s[13] = t[1];
    // ligne 2 decalee de 2
    s[2] = t[10]; s[6] = t[14]; s[10] = t[2]; s[14] = t[6];
    // ligne 3 decalee de 3
    s[3] = t[15]; s[7] = t[3]; s[11] = t[7]; s[15] = t[11];
}

fn mix_columns(s: &mut [u8; 16]) {
    for c in 0..4 {
        let i = 4 * c;
        let a0 = s[i]; let a1 = s[i + 1]; let a2 = s[i + 2]; let a3 = s[i + 3];
        s[i] = gmul(a0, 2) ^ gmul(a1, 3) ^ a2 ^ a3;
        s[i + 1] = a0 ^ gmul(a1, 2) ^ gmul(a2, 3) ^ a3;
        s[i + 2] = a0 ^ a1 ^ gmul(a2, 2) ^ gmul(a3, 3);
        s[i + 3] = gmul(a0, 3) ^ a1 ^ a2 ^ gmul(a3, 2);
    }
}

/// Auto-test : vecteur FIPS-197 AES-128 et AES-256.
pub fn selftest() -> Result<(), &'static str> {
    // AES-128 FIPS-197 appendix B/C.1
    let key = [
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
    ];
    let mut block = [
        0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff,
    ];
    let aes = Aes::new(&key);
    aes.encrypt_block(&mut block);
    let want = [
        0x69, 0xc4, 0xe0, 0xd8, 0x6a, 0x7b, 0x04, 0x30, 0xd8, 0xcd, 0xb7, 0x80, 0x70, 0xb4, 0xc5, 0x5a,
    ];
    if block != want { return Err("aes-128"); }

    // AES-256 FIPS-197 C.3
    let key = [
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f,
        0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f,
    ];
    let mut block = [
        0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff,
    ];
    let aes = Aes::new(&key);
    aes.encrypt_block(&mut block);
    let want = [
        0x8e, 0xa2, 0xb7, 0xca, 0x51, 0x67, 0x45, 0xbf, 0xea, 0xfc, 0x49, 0x90, 0x4b, 0x49, 0x60, 0x89,
    ];
    if block != want { return Err("aes-256"); }
    Ok(())
}
